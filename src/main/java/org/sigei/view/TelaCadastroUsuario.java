    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String nome = jTextField1.getText(); 
String cpf = jTextField2.getText();  
String endereco = jTextField3.getText(); 
String nomeCadastro = jTextField5.getText(); 
String senha = jTextField6.getText();
String tipo = (String) comboTipoUsuario.getSelectedItem();

// Aqui voc√™ validaria os campos e criaria o objeto Pessoa e Usuario
try {
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        usuarioDAO.inserirNovoUsuario(nome, cpf, endereco, nomeCadastro, senha, tipo);

        JOptionPane.showMessageDialog(this, "Usu√°rio cadastrado com sucesso!");
        this.dispose();
    } catch (HeadlessException e) {
        JOptionPane.showMessageDialog(this, "Erro ao cadastrar: " + e.getMessage());
    }
}
